    for key in ["P0", "P1", "P2", "P3"]:
        summary_rows.append([f"优先级-{key}", priority_count.get(key, 0)])
    for item in summary_rows:
        summary.append(item)
    for r in range(2, summary.max_row + 1):
        for c in range(1, 3):
            summary.cell(r, c).alignment = Alignment(vertical="center", wrap_text=True)
            summary.cell(r, c).border = Border(left=thin, right=thin, top=thin, bottom=thin)
    summary.column_dimensions["A"].width = 24
    summary.column_dimensions["B"].width = 22

