param(
    [string]$RepoRoot = "",
    [string]$OutputPath = "",
    [ValidateSet("core", "demo", "all")]
    [string]$Mode = "all"
)

Set-StrictMode -Version Latest
$ErrorActionPreference = "Stop"

if ([string]::IsNullOrWhiteSpace($RepoRoot)) {
    $RepoRoot = (Resolve-Path (Join-Path $PSScriptRoot "..\..")).Path
}

$migrationRoot = Join-Path $RepoRoot "cost_platform_back\cost_admin\src\main\resources\db\migration"
$sqlFiles = Get-ChildItem -Path $migrationRoot -Filter "*.sql" | Sort-Object Name
if ($sqlFiles.Count -eq 0) {
    throw "No migration SQL files found under $migrationRoot"
}

$coreExcludedNames = @(
    "V20260330_002__demo_configuration_examples.sql",
    "V20260405_018__replace_demo_with_shougang_hr_costs.sql"
)

$demoExcludedNames = @(
    "V20260330_002__demo_configuration_examples.sql"
)

function Get-SnapshotFiles {
    param(
        [System.IO.FileInfo[]]$Files,
        [string[]]$ExcludedNames
    )

    return $Files | Where-Object { $ExcludedNames -notcontains $_.Name }
}

function Build-SnapshotContent {
    param(
        [string]$SnapshotName,
        [string]$Purpose,
        [System.IO.FileInfo[]]$Files
    )

    $builder = New-Object System.Text.StringBuilder
    [void]$builder.AppendLine("-- $SnapshotName")
    [void]$builder.AppendLine("-- Generated by build-cost-init.ps1")
    [void]$builder.AppendLine("-- Purpose: $Purpose")
    [void]$builder.AppendLine("-- Notes:")
    [void]$builder.AppendLine("-- 1. Run on an empty database.")
    [void]$builder.AppendLine("-- 2. This file is a snapshot convenience entrypoint, while Flyway remains the source of truth.")
    [void]$builder.AppendLine("-- 3. After generation, verify the latest migration list and regenerate when new migrations are added.")
    [void]$builder.AppendLine()

    foreach ($sqlFile in $Files) {
        $content = Get-Content -Path $sqlFile.FullName -Encoding UTF8 -Raw
        [void]$builder.AppendLine("-- >>> BEGIN " + $sqlFile.Name)
        [void]$builder.AppendLine($content.TrimEnd())
        [void]$builder.AppendLine("-- <<< END " + $sqlFile.Name)
        [void]$builder.AppendLine()
    }

    return $builder.ToString()
}

function Write-SnapshotFile {
    param(
        [string]$TargetPath,
        [string]$Content
    )

    $parentDir = Split-Path -Parent $TargetPath
    if (-not (Test-Path $parentDir)) {
        New-Item -ItemType Directory -Force -Path $parentDir | Out-Null
    }

    Set-Content -Path $TargetPath -Value $Content -Encoding UTF8
    Write-Host "Generated cost init snapshot: $TargetPath"
}

$coreFiles = Get-SnapshotFiles -Files $sqlFiles -ExcludedNames $coreExcludedNames
$demoFiles = Get-SnapshotFiles -Files $sqlFiles -ExcludedNames $demoExcludedNames

$defaultCoreOutputPath = Join-Path $RepoRoot "cost_platform_back\cost_admin\src\main\resources\db\cost_init.sql"
$defaultDemoOutputPath = Join-Path $RepoRoot "cost_platform_back\cost_admin\src\main\resources\db\cost_init_demo.sql"

switch ($Mode) {
    "core" {
        if ([string]::IsNullOrWhiteSpace($OutputPath)) {
            $OutputPath = $defaultCoreOutputPath
        }
        $content = Build-SnapshotContent -SnapshotName "cost_init.sql" -Purpose "initialize a blank database with platform-required schema, menus, dictionaries and governance metadata only." -Files $coreFiles
        Write-SnapshotFile -TargetPath $OutputPath -Content $content
    }
    "demo" {
        if ([string]::IsNullOrWhiteSpace($OutputPath)) {
            $OutputPath = $defaultDemoOutputPath
        }
        $content = Build-SnapshotContent -SnapshotName "cost_init_demo.sql" -Purpose "initialize a blank database with platform-required assets plus test scene, fee, variable, formula and rule configuration for demonstrations." -Files $demoFiles
        Write-SnapshotFile -TargetPath $OutputPath -Content $content
    }
    "all" {
        if (-not [string]::IsNullOrWhiteSpace($OutputPath)) {
            throw "OutputPath is only supported when Mode is 'core' or 'demo'."
        }
        $coreContent = Build-SnapshotContent -SnapshotName "cost_init.sql" -Purpose "initialize a blank database with platform-required schema, menus, dictionaries and governance metadata only." -Files $coreFiles
        $demoContent = Build-SnapshotContent -SnapshotName "cost_init_demo.sql" -Purpose "initialize a blank database with platform-required assets plus test scene, fee, variable, formula and rule configuration for demonstrations." -Files $demoFiles
        Write-SnapshotFile -TargetPath $defaultCoreOutputPath -Content $coreContent
        Write-SnapshotFile -TargetPath $defaultDemoOutputPath -Content $demoContent
    }
}
